


n1 = int(input("Escribe tu primer numero: "))
n2 = int(input("Escribe tu segundo numero: "))
n3 = int(input("Escribe tu tercer numero: "))

nunMasgrande = max(n1, n2, n3)
nunMaspequeno = min(n1, n2, n3)


print("El numero mas grande es: ", nunMasgrande)
print("El numero mas pequeno es: ", nunMaspequeno)